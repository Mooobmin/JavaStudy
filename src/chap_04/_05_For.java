        System.out.println("어서오세요. 나코입니다.");
        System.out.println("어서오세요. 나코입니다.");
        System.out.println("어서오세요. 나코입니다.");

        //매장 인사법이 바뀌면?
        System.out.println("환영합니다 나코입니다.");
